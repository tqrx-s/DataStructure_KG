# 中文分词
texts = [[word for word in jieba.cut(document)] for document in text_corpus]
print("分词后的文本:\n", texts)

# 创建字典
dictionary = corpora.Dictionary(texts)
print("\n字典中的词与ID映射:\n", dictionary.token2id)

# 生成文档的向量表示
bow_corpus = [dictionary.doc2bow(text) for text in texts]
print("\n文档的词袋表示（BoW）:\n", bow_corpus)

# 初始化TF-IDF模型
from gensim import models
tfidf = models.TfidfModel(bow_corpus)

# 将BoW文档转化为TF-IDF向量
tfidf_corpus = [tfidf[bow] for bow in bow_corpus]
print("\nTF-IDF向量:\n", list(tfidf_corpus))

# 创建相似度索引
index = similarities.SparseMatrixSimilarity(tfidf[bow_corpus], num_features=len(dictionary))

# 查询文档
query_document = "周一15:30；数据结构；三教；学生行为包括交头接耳、认真听讲；老师行为包括提问同学；课堂测验分数为80；"
query_bow = dictionary.doc2bow(jieba.cut(query_document))
print("\n查询文档的词袋表示（BoW）:\n", query_bow)

# 计算TF-IDF
query_tfidf = tfidf[query_bow]
print("\n查询文档的TF-IDF表示:\n", query_tfidf)

# 查询相似度
sims = index[query_tfidf]
print("\n与每个文档的相似度分数:\n", list(enumerate(sims)))

# 输出相似度结果
sorted_sims = sorted(enumerate(sims), key=lambda x: x[1], reverse=True)
for document_number, score in sorted_sims:
    print(f"文档 {document_number} 的相似度分数: {score:.4f}")

